require('dotenv').config();
const { Client } = require('pg');

const client = new Client({
  connectionString: process.env.POSTGRES_URL,
});

async function createTable() {
  try {
    await client.connect();
    console.log("Connected to database...");

    const createTableQuery = `
      CREATE TABLE IF NOT EXISTS announcements (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        title TEXT NOT NULL,
        content TEXT NOT NULL,
        active BOOLEAN DEFAULT true,
        start_date TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
        end_date TIMESTAMP WITH TIME ZONE,
        created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
        updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
      );
    `;

    await client.query(createTableQuery);
    console.log("✅ Table 'announcements' created successfully!");
    
    // Insert a sample announcement
    const insertQuery = `
      INSERT INTO announcements (title, content, active)
      VALUES ('Selamat Datang', 'Selamat datang di Vending Machine Pintar!', true)
      ON CONFLICT DO NOTHING;
    `;
    
    await client.query(insertQuery);
    console.log("✅ Sample announcement inserted.");

  } catch (err) {
    console.error("❌ Error creating table:", err);
  } finally {
    await client.end();
  }
}

createTable();
